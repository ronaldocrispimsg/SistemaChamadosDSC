    private void atualizarTabela() {
        try {
            UsuarioDAO dao = new UsuarioDAO();
            List<Usuario> lista = dao.buscarTodos(); // Certifique-se que esse m√©todo existe no seu DAO
            
            // Pega o modelo da tabela que o NetBeans criou
            DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
            
            // Limpa a tabela antes de carregar
            modelo.setRowCount(0);
            
            for (Usuario u : lista) {
                modelo.addRow(new Object[]{
                    u.getId(),
                    u.getNome(),
                    u.getLogin(),
                    u.getSetor() != null ? u.getSetor().getNome() : "Sem Setor"
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar tabela: " + e.getMessage());
        }
    }
