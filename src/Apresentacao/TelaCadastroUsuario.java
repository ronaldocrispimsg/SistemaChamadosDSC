    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    // 1. Coletar os dados da tela
        String nome = txtNome.getText();
        String login = txtLogin.getText();
        String senhaPlana = new String(txtSenha.getPassword());
        
        // Aqui pegamos o objeto Setor que foi selecionado
        Setor setorSelecionado = (Setor) cbSetor.getSelectedItem();

        // 2. Validar campos básicos
        if (nome.isEmpty() || login.isEmpty() || senhaPlana.isEmpty() || setorSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos e selecione um setor!");
            return;
        }

        // 3. Criar o objeto de domínio e criptografar a senha
        Usuario novoUsuario = new Usuario();
        novoUsuario.setNome(nome);
        novoUsuario.setLogin(login);
        novoUsuario.setSenha(Criptografia.md5(senhaPlana)); // SEGURANÇA!
        novoUsuario.setSetor(setorSelecionado);

        // 4. Salvar no Banco via DAO
        try {
            UsuarioDAO dao = new UsuarioDAO();
            dao.salvar(novoUsuario);
            JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");
            
            // Limpar campos
            txtNome.setText("");
            txtLogin.setText("");
            txtSenha.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar: " + e.getMessage());
            logger.log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
    
