    /**
     * Busca os setores no banco de dados e preenche o ComboBox
     */
    private void carregarSetores() {
        try {
            SetorDAO sDao = new SetorDAO();
            List<Setor> setores = sDao.buscarTodos();
            
            cbSetor.removeAllItems(); // Remove os "Item 1, 2" do NetBeans
            for (Setor s : setores) {
                cbSetor.addItem(s); // Adiciona o objeto Setor completo
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar setores: " + e.getMessage());
            logger.log(Level.SEVERE, null, e);
        }
    }
